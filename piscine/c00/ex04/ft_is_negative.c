#include <unistd.h>

void	ft_is_negative(int x)
{
	char	a;
	char	c;

	a = 'P';
	c = 'N';
	if (x >= 0)
	{
		write(1, &a, 1);
	}
	else
	{
		write(1, &c, 1);
	}
}
